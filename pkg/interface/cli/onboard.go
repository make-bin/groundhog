	cfg.DBPassword = prompt(scanner, "  Password", "openclaw")
	cfg.DBName = prompt(scanner, "  Database name", "openclaw")
	cfg.DBSSLMode = promptChoice(scanner, "  SSL mode", []string{"disable", "require", "verify-full"}, "disable")

