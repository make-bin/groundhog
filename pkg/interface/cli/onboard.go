// prompt prints a question with a default value and reads one line from the scanner.
// If the user presses Enter without input, the default is returned.
func prompt(scanner *bufio.Scanner, question, defaultVal string) string {
	if defaultVal != "" {
		fmt.Printf("%s [%s]: ", question, defaultVal)
	} else {
		fmt.Printf("%s: ", question)
	}

	if scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line != "" {
			return line
		}
	}
	return defaultVal
}

// promptChoice shows a list of valid choices and loops until the user picks one.
func promptChoice(scanner *bufio.Scanner, question string, choices []string, defaultVal string) string {
	choiceStr := strings.Join(choices, "/")
	for {
		fmt.Printf("%s (%s) [%s]: ", question, choiceStr, defaultVal)

		if scanner.Scan() {
			line := strings.TrimSpace(scanner.Text())
			if line == "" {
				return defaultVal
			}
			for _, c := range choices {
				if strings.EqualFold(line, c) {
					return c
				}
			}
			fmt.Printf("  %s✗%s  Invalid choice. Please enter one of: %s\n", colorRed, colorReset, choiceStr)
		} else {
			return defaultVal
		}
	}
}

// writeConfig renders the config template and writes it to outputPath.
func writeConfig(cfg *onboardConfig, outputPath string) error {
	tmpl, err := template.New("config").Parse(configTemplate)
	if err != nil {
		return fmt.Errorf("template parse error: %w", err)
	}

	f, err := os.Create(outputPath)
	if err != nil {
		return fmt.Errorf("cannot create file %s: %w", outputPath, err)
	}
	defer f.Close()

	return tmpl.Execute(f, cfg)
}

