// NewOnboardCommand creates the "openclaw onboard" command.
func NewOnboardCommand() *cobra.Command {
	var configPath string
	var outputPath string

	cmd := &cobra.Command{
		Use:   "onboard",
		Short: "Interactive setup wizard for OpenClaw",
		Long:  "Interactively guide you through database, Redis, JWT, and model provider configuration, then generate configs/config.yaml.",
		RunE: func(cmd *cobra.Command, args []string) error {
			return runOnboard(configPath, outputPath)
		},
	}

	cmd.Flags().StringVar(&configPath, "config", "configs/config.yaml", "path to configuration file (used by doctor after onboard)")
	cmd.Flags().StringVar(&outputPath, "output", "configs/config.yaml", "path to write the generated config file")

	return cmd
}

